package basicprograms;

/**
 * 
 * This is a documentation comment.
 * This class demonstrates different types of comments in Java.
 */
public class Comments {

    public static void main(String[] args) {

        // This is a single-line comment
        System.out.println("Single-line comment example");

        /*
         * This is a multi-line comment
         * It can span multiple lines
         */
        System.out.println("Multi-line comment example");

        System.out.println("Documentation comment shown above the class");
    }
}

