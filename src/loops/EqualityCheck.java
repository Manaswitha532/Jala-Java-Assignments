package loops;

public class EqualityCheck {

	    public static void main(String[] args) {
	        int x = 10;
	        int y = 20;
	        int z = 10;

	        // Equal operator ==
	        System.out.println("x == y: " + (x == y));  // false
	        System.out.println("x == z: " + (x == z));  // true

	        // Not equal operator !=
	        System.out.println("x != y: " + (x != y));  // true
	        System.out.println("x != z: " + (x != z));  // false
	    }
	}


